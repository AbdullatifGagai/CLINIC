        public string Master_DoctorSave(BValues bv, string ip, string cInfo, Doctor Doctor, List<DoctorDayDatails> DoctorDayDatails)
        {
            var parameters = new { x = Doctor, DoctorDayDatails };



            foreach (var item in DoctorDayDatails)
            {



                item.TenantId = bv.TenantId; item.AppId = bv.AppId; item.CreatedBy = bv.UserId;
            }






            Doctor.TenantId = bv.TenantId; Doctor.AppId = bv.AppId; Doctor.CreatedBy = bv.UserId;

            try
            {
                TenantId = bv.TenantId; AppId = bv.AppId; CreatedBy = bv.UserId; ModifiedBy = bv.UserId;

                string message = DataBase.ExecuteQuery<Doctor>(parameters, Connection.GetConnection()).FirstOrDefault().ReturnMessage;

                Loging.DB_Log(eLogType.Log_Posetive, "", parameters, "", MODULE, Connection.GetLogConnection(), cInfo, ip, bv.UserId);

                return message;
            }
            catch (Exception ex)
            {
                Loging.DB_Log(eLogType.Log_Negative, ex.Message, parameters, "", MODULE, Connection.GetLogConnection(), cInfo, ip, bv.UserId);

                return ex.Message;
            }
        }













